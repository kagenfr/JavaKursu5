package Day15;

public class _02_JavaNestedLoop {
    public static void main(String[] args) {
        //2 lerin çarpım tablosunu sormuştuk.
        //5 lere kadar olan halini yapınız.


        for (int j = 1; j <=5; j++) {

            System.out.println();

            for (int i = 1; i <= 10; i++) {
                System.out.println(j+"x" + i + "=" + j * i);








            }
        }
    }
}
